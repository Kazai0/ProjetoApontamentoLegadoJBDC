package br.com.domain.model;

public class LancarErroModel {

	static private String nameErro;
	static private String obsErro;
	static private String codigoProduto;

	
	
	
	public String getNameErro() {
		return nameErro;
	}

	public void setNameErro(String nameErro) {
		this.nameErro = nameErro;
	}

	public String getObsErro() {
		return obsErro;
	}

	public void setObsErro(String obsErro) {
		this.obsErro = obsErro;
	}

	public String getCodigoProduto() {
		return codigoProduto;
	}

	public void setCodigoProduto(String codigoProduto) {
		this.codigoProduto = codigoProduto;
	}

	
	
	
}
